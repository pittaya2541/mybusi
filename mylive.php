<p/>การที่ทรัพย์สินสุทธิของเกตส์ มีมูลค่าลดลงตั้งแต่ปี ค.ศ. 2000 เป็นต้นมา มีสาเหตุมาจากการที่หุ้นของไมโครซอฟท์มีราคาลดลง รวมถึงการที่เขาได้บริจาคเงินหลายพันล้านดอลลาร์ให้องค์กรการกุศลของเขา และแม้เขาจะมีรายได้ลดลง ตามรายงานของนิตยสารฟอร์บในปีค.ศ. 2004 เกตส์ยังได้บริจาคเงินรวมกว่า 28,000 ล้านดอลลาร์สหรัฐให้กับองค์กรการกุศลในช่วงไม่กี่ปีที่ผ่านมา เขาได้กลายเป็นบุคคลที่มั่งคั่งที่สุดในโลกไปเสียแล้ว แม้จะนับเอาประมุขของรัฐ (ผู้ซึ่งทรัพย์สินมาจากสถานะทางสังคม) ไว้ในการจัดอันดับด้วยก็ตาม (แม้ว่าการจัดอันดับตามมาตรฐานของนิตยสารฟอร์บนั้น จะไม่รวมเอาประมุขของรัฐเอาไว้ด้วย ฟอร์บได้จัดทำบัญชีประมาณการทรัพย์สินของประมุขแต่ละประเทศไว้ต่างหาก เมื่อนำรายชื่อจากการจัดอันดับทั้งสองแบบมารวมกันแล้ว พบว่าเกตส์เป็นบุคคลที่มั่งคั่งที่สุดในโลก) แต่ใน ค.ศ. 2016 เขาได้กลับมามีเงิน 76.6 พันล้านดอลลาร์สหรัฐ(แปลมาจากวิกิพีเดียเวอร์ชันภาษาอังกฤษ)
