<br><font size=4>Mark zuckerberg</font>
<br>มาร์ก เอลเลียต ซักเคอร์เบิร์ก (อังกฤษ: Mark Elliot Zuckerberg) เกิดเมื่อวันที่ 14 พฤษภาคม ค.ศ. 1984 ที่ ประเทศอเมริกา เป็นนักธุรกิจชาวอเมริกันเป็นที่รู้จักในฐานะผู้ก่อตั้งเว็บไซต์ เฟซบุ๊ก เขาร่วมก่อตั้งเฟสบุ๊กร่วมกับเพื่อนอีก 4 คน ขณะกำลังศึกษาที่มหาวิทยาลัยฮาร์วาร์ด นิตยสารไทม์ ได้ให้เขาเป็นบุคคลแห่งปี ค.ศ. 2010 ที่โรงเรียนอาร์ดสลีย์ไฮสคูล เขาได้มีความสามารถด้านการศึกษาคลาสสิก ก่อนที่เขาจะย้ายไปเรียนระดับมัธยมปลายที่โรงเรียนฟิลิปส์เอกเซกเตอร์อคาเดมี ที่นี่ซักเคอร์เบิร์กได้รับรางวัลทางวิทยาศาสตร์ (คณิตศาสตร์ ดาราศาสตร์ และฟิสิกส์) และศึกษาด้านศิลปะคลาสสิก เขายังเรียนภาษาต่างประเทศ โดยเขาสามารถอ่าน ภาษาฝรั่งเศส ภาษาฮิบรู ภาษาละติน และภาษากรีกโบราณ เขายังเป็นกัปตันทีมฟันดาบ
ในงานสังสรรค์ในช่วงชั้นปีที่ 2 ซักเคอร์เบิร์กพบกับพริสซิลลา ชาน ที่ต่อมาเป็นเพื่อนหญิงของเขา ในเดือนกันยายน ค.ศ. 2010 ชานซึ่งศึกษาแพทย์ ได้ย้ายมาอยู่บ้านเช่าของซักเคอร์เบิร์กในแพโลอัลโต
ซักเคอร์เบิร์กสามารถมองเห็นสีฟ้าได้ดีที่สุด เพราะเขาเป็นโรคตาบอดสีซึ่งมองสีแดงและสีเขียวได้ไม่ชัดเจน นอกจากนี้สีฟ้ายังเป็นสีหลักในเว็บไซต์เฟซบุ๊กอีกด้วย
ซักเคอร์เบิร์กได้เปิดตัวเฟซบุ๊ก จากในห้องพักของเขาในมหาวิทยาลัยฮาวาร์ดเมื่อวันที่ 4 กุมภาพันธ์ ค.ศ. 2004 แรงบันดาลใจแรก ๆ ของเฟซบุ๊กอาจมาจากที่โรงเรียนฟิลิปส์เอกเซกเตอร์อคาเดมี ที่เขาเรียนจบปี ค.ศ. 2002 โดยที่เผยในเว็บไซต์ของเขาคือ สารบัญรูปนักศึกษาของเขา ที่นักศึกษาหมายถึง "เดอะเฟซบุ๊ก" มีสารบัญภาพ ที่มีภาพนักศึกษาทำกิจกรรมในหลาย ๆ โรงเรียน โดยนักศึกษาสามารถเข้ามาให้ข้อมูล อย่างเช่น ชั้นปีที่ศึกษา เพื่อนใกล้ชิด หมายเลขโทรศัพท์ โดยในขณะนั้น เฟซบุ๊ก เริ่มต้นเพียงแค่ "Harvard thing" จนกระทั่งซักเคอร์เบิร์กตัดสินใจที่จะกระจายไปสู่มหาวิทยาลัยอื่นๆได้รับความช่วยเหลือจากเพื่อนร่วมห้อง ดัสติน มอสโควิตซ์ โดยเริ่มจากมหาวิทยาลัยสแตนฟอร์ด มหาวิทยาลัยดาร์ตมัธ มหาวิทยาลัยโคลัมเบีย มหาวิทยาลัยนิวยอร์ก มหาวิทยาลัยคอร์เนล มหาวิทยาลัยบราวน์ และมหาวิทยาลัยเยล จากนั้นก็เข้าสู่โรงเรียนอื่น ที่มีความสัมพันธ์กับมหาวิทยาลัยฮาร์วาร์ด
ซักเคอร์เบิร์กได้ย้ายไปอยู่ที่เมืองแพโลอัลโต รัฐแคลิฟอร์เนีย กับมอสโควิตซ์และเพื่อนบางส่วน พวกเขาดัดแปลงบ้านเช่าเป็นสำนักงาน ในฤดูร้อนนั้น ซักเคอร์เบิร์กได้พบกับปีเตอร์ ทีล ที่ได้ให้ทุนกับบริษัท พวกเขาเริ่มก่อตั้งบริษัทแรกในกลางปี 2004 พวกเขาได้ปฏิเสธการเสนอขายเฟซบุ๊กกับบริษัทใหญ่ ๆ โดยในบทสัมภาษณ์ในปี 2007 ซักเคอร์เบิร์กอธิบายไว้ว่า
เขาพูดในนิตยสารไวร์ ในปี 2010 ว่า "สิ่งที่ผมใส่ใจมากเกี่ยวกับภารกิจนี้ ก็คือทำให้โลกเปิดกว้างขึ้น" ก่อนหน้านั้นในเดือนเมษายน 2009 ซักเคอร์เบิร์กได้สอบถามคำแนะนำจากผู้บริหารระดับสูงด้านการเงินของเน็ตสเคป ปีเตอร์ เคอร์รี เกี่ยวกับยุทธวิธีสำหรับเฟซบุ๊ก
<p/>เมื่อวันที่ 21 กรกฎาคม ค.ศ. 2010 ซักเคอร์เบิร์กรายงานว่า บริษัทมีผู้ใช้ 500 ล้านบัญชีรายชื่อ และเมื่อถามว่า เฟซบุ๊ก จะสามารถทำเงิน หรือสร้างปรากฏการณ์เพิ่มขึ้น เขาอธิบายว่า:ผมคิดว่า เราสามารถ... ถ้าคุณดูว่าโฆษณาที่มีในแต่ละหน้ากินไปขนาดไหน เมื่อเปรียบเทียบกับการค้นหาข้อมูล ของเรามีน้อยกว่าร้อยละ 10 ต่อหน้าและยอดการค้นหาปกติจะมีโฆษณาราวร้อยละ 20 ... นี่เป็นสิ่งง่ายที่ทุกคนจะทำ แต่เราไม่ใช่อย่างนั้น เราทำเงินให้พอที่เราจะดำเนินงานได้ เติบโตในอัตราที่เราต้องการ
<p/>ในปี 2010 สตีเฟน เลวี ผู้แต่งหนังสือเรื่อง Hackers: Heroes of the Computer Revolution ในปี ค.ศ. 1984 ได้เขียนเกี่ยวกับซักเคอร์เบิร์กไว้ว่า "เห็นชัดว่าเขาคิดว่าตัวเองเป็นแฮ็กเกอร์" ซักเคอร์เบิร์กพูดว่า "มันโอเคที่จะสร้างสิ่งใหม่...ทำให้มันดียิ่งขึ้น"เฟซบุ๊กเริ่มให้มี "แฮ็กคาธอน" ในทุก ๆ 6 ถึง 8 อาทิตย์ เปิดโอกาส 1 คืนให้ร่วมคิดและจบโครงการ 1 โครง โดยบริษัทให้จัดหาเพลง อาหารและเบียร์ สำหรับงานแฮกคาธอน และจะมีเจ้าหน้าที่ของเฟซบุ๊ก รวมถึงซักเคอร์เบิร์ก เข้าร่วมด้วย "แนวคิดคือคุณสามารถสร้างบางสิ่งให้ดีได้ใน 1 คืน" ซักเคอร์เบิร์กบอกเลวี "และเป็นบุคลิกของเฟซบุ๊กในปัจจุบัน...ซึ่งก็คือนิสัยส่วนตัวของผมด้วย"
<p/>ในปี นิตยสาร วานิตีแฟร์ ได้ให้ซักเคอร์เบิร์กติดอันดับ 1 ในปี 2010 ของรายชื่อ "100 อันดับ บุคคลที่มีอิทธิพลที่สุดในยุคข้อมูล" ในปี 2010 ยังติดอันดับ 16 ของการสำรวจประจำปีของ นิวสเตตส์เม็น ในหัวข้อ "บุคคลที่มีอิทธิพลมากที่สุดในโลก 50 อันดับ"
