<br><font size=4>bill gates</font>
<br>วิลเลียม เฮนรี เกตส์ ที่สาม (เกิด 28 ตุลาคม ค.ศ. 1955) หรือที่มักเป็นที่รู้จักในชื่อ บิล เกตส์ เป็นนักธุรกิจชาวอเมริกัน และหนึ่งในผู้ก่อตั้งบริษัทไมโครซอฟท์ เขากับผู้บุกเบิกด้านคอมพิวเตอร์ส่วนบุคคลคนอื่น ๆ ได้ร่วมกันเขียนต้นแบบของภาษาอัลแตร์เบสิก ซึ่งเป็นอินเตอร์เพรเตอร์สำหรับเครื่องอัลแตร์ 8800 (เครื่องคอมพิวเตอร์ส่วนบุคคลในยุคแรกๆ) เขาได้ร่วมกับพอล แอลเลน ก่อตั้งไมโครซอฟท์ คอร์ปอเรชันขึ้น ซึ่งในขณะนี้เขาดำรงตำแหน่งที่ปรึกษาเรื่องเทคโนโลยี นิตยสารฟอบส์ได้จัดอันดับให้ บิล เกตส์ เป็นบุคคลที่ร่ำรวยที่สุดในโลกหลายปีติดต่อกันวิลเลียม เฮนรี เกตส์ ที่สามได้รับการแต่งตั้งเป็นผู้บัญชาการอัศวินแห่งจักรวรรดิบริเตน (KBE) จากสมเด็จพระราชินีนาถอลิซาเบธที่ 2
